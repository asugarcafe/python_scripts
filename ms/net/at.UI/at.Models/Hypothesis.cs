using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace at.Models {
   public class Hypothesis {


      /*
       * it should be possible to gather evidence that could either support or refute the proposed relationship between variables
       * 
      Key attributes of a hypothesis:
      Testability: A hypothesis must be able to be tested through experimentation or observation to determine if it is supported by evidence. 
      Falsifiable: There should be a potential for evidence to disprove the hypothesis. 
      Clarity: The hypothesis should be stated in a clear and concise manner, avoiding ambiguity. 
      Specificity: It should clearly define the variables involved and the expected relationship between them. 
      Empirical basis: The hypothesis should be grounded in existing knowledge and observations. 
      Relevance: The hypothesis should be relevant to the research question being investigated. 
      Directional: When appropriate, a hypothesis should specify the direction of the relationship between variables (e.g., "increased exercise leads to improved mood"). 
      */
   }
}
